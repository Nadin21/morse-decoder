const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let array_expr = expr.match(/.{10}/g);
    let array_morse = array_expr.map(function(item){
        return item.replace(/^0*/g,'')
    })
    let result = '';
    array_morse.forEach(element => {
        if(element == '**********') result+=' '
        else {
            let code  = element.replace(/11/g,'-').replace(/10/g,'.')
            result+=MORSE_TABLE[code]
        }
    });
    return result
}

module.exports = {
    decode
}